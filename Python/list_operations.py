# Example of list operations and methods
my_list = [1, 2, 3, 4, 5]

# Append an element
my_list.append(6)

# Remove an element
my_list.remove(3)

# Sort the list
my_list.sort()

# Find the index of an element
index = my_list.index(4)

print("List:", my_list)
print("Index of 4:", index)
